mov cx, WORD PTR [bp - 4]
add bx, 8
call probepixel
cmp es:[bx], BYTE PTR 54
je processdirection
mov bx, WORD PTR [bp - 2]
mov cx, WORD PTR [bp - 4]
add bx, 8
add cx, 7
call probepixel
cmp es:[bx], BYTE PTR 54
je processdirection
mov WORD PTR [bp - 6], 2
mov WORD PTR [bp - 8], 0
jmp processdirection


done:

mov ax, 3    ;reset to text mode
int 10h

mov ah, 4ch  ;exit to DOS
int 21h


