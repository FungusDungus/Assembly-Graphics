; Converts x y coordinates into an offset (y * 320 + x) 
; uses and accepts not preserved: bx -> x val, ax -> y val | returns: cx -> pixel offset from 0a000h segment
coordtranslate proc
mov cl, 6
shl ax, cl
add bx, ax
mov cl, 2
shl ax, cl
add bx, ax
ret
coordtranslate endp

; Writes a sprite to video memory
; uses and accepts not preserved: bx -> start pos, ax -> width, bp -> height, dx -> sprite offset in data | uses not preserved: ch
drawsprite proc

mov di, bx
mov si, dx
mov bx, 320
mov dx, ax
sub bx, dx
draw:
mov ch, ds:[si]
cmp colorindex, -1
je skp0
cmp ch, 62
jne skp0
push bx
mov bx, colorindex
mov ch, ghostcolors[bx]
pop bx
skp0:
cmp erasemode, 1
je skp
xor ch, es:[di]
skp:
mov es:[di], ch
inc si
inc di
dec ax
cmp ax, 0
je newline
jmp draw

newline:
mov ax, dx
dec bp
add di, bx
cmp bp, 0
je skip
jmp draw
skip:

ret
drawsprite endp

